#!/bin/sh
set -e

#--------------------------------------------------------------------------------------------------
# Settings
#--------------------------------------------------------------------------------------------------

name="rembg"

repository="https://github.com/danielgatis/rembg.git"

code="$PWD/code"

options="gpu,cli"

#--------------------------------------------------------------------------------------------------
# Syntax
#--------------------------------------------------------------------------------------------------

read -p "Build $name ? (yes/no) " REPLY

if [ "$REPLY" != "yes" ]; then exit 1; fi

#--------------------------------------------------------------------------------------------------
# Clean
#--------------------------------------------------------------------------------------------------

rm -rf   "$code"
mkdir -p "$code"
touch    "$code"/.gitignore

cd "$code"

#--------------------------------------------------------------------------------------------------
# Clone
#--------------------------------------------------------------------------------------------------

git clone "$repository"

cd "$name"

#--------------------------------------------------------------------------------------------------
# Environment
#--------------------------------------------------------------------------------------------------

python -m venv .venv

if [ -f ".venv/Scripts/activate" ]; then

   # Windows / Git Bash
  . ".venv/Scripts/activate"
else
  . ".venv/bin/activate"
fi

#--------------------------------------------------------------------------------------------------
# Install
#--------------------------------------------------------------------------------------------------

python -m pip install --upgrade pip

python -m pip install ".[$options]"
